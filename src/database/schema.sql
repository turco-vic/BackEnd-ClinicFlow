CREATE DATABASE clinicflow;

\c clinicflow;

CREATE TYPE role AS ENUM ('MEDICO', 'PACIENTE');

CREATE TABLE users (
    id SERIAL PRIMARY KEY,
    nome TEXT,
    email TEXT UNIQUE,
    password TEXT,
    role role DEFAULT 'PACIENTE'
);

CREATE TABLE patients (
    id SERIAL PRIMARY KEY,
    user_id INT UNIQUE NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    birth_date DATE NOT NULL,
    cpf VARCHAR(14) NOT NULL UNIQUE,
    number_phone VARCHAR(20) NOT NULL
);

CREATE TABLE especialtys (
    id SERIAL PRIMARY KEY,
    especialty VARCHAR(255) NOT NULL
);

CREATE TABLE doctors (
    id SERIAL PRIMARY KEY,
    user_id INT UNIQUE NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    name VARCHAR(255) NOT NULL,
    birth_date DATE NOT NULL,
    number_phone VARCHAR(20) NOT NULL,
    especialty_id INT REFERENCES especialtys(id),
    doctor_photo TEXT
);

CREATE TABLE schedules (
    id SERIAL PRIMARY KEY,
    patient_id INT NOT NULL REFERENCES patients(id) ON DELETE CASCADE,
    doctor_id INT NOT NULL REFERENCES doctors(id) ON DELETE CASCADE,
    consult_date DATE NOT NULL,
    consult_hour TIME NOT NULL
);

CREATE TABLE medicalrecords (
    id SERIAL PRIMARY KEY,
    patient_info TEXT NOT NULL,
    patient_id INT NOT NULL REFERENCES patients(id) ON DELETE CASCADE
);

